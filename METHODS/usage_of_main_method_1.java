public class usage_of_main_method_1
{
    public static void main(String [] args)
    {
        System.out.println("\nI am main");
    }
}

// public - It is the access modifier which specifies the main method is public and can be access by any 
//         method.

// static - It means the it belongs to a particular class and not to any object.

// void  -  void means empty. This keyword specifies that the main class doesn't return anything.

// main - It the name of the method. In a java program there should at least one main method because this 
//        is the place where the execution of the program begins.

// String[] args - It stores java command line argument and it is array of type java.lang.String. we can name
//         args anything we want.